				<h2 id="about">About <em>Arabidopsis thaliana</em></h2>
				<p><em>Arabidopsis thaliana</em> is a small flowering plant that is widely used as a model organism in plant biology. Arabidopsis is a member of the mustard (Brassicaceae) family, which includes cultivated species such as cabbage and radish. Arabidopsis is not of major agronomic significance, but it offers important advantages for basic research in genetics and molecular biology. <em>Arabidopsis thaliana</em> has a genome size of ~135 Mbp, and a haploid chromosome number of 5.
				</p>
				<p>Taxonomy ID <a href="http://www.uniprot.org/taxonomy/3702" rel="external" class="constant">3702</a></p><p>Data source <a href="http://www.arabidopsis.org/" rel="external">TAIR</a></p><p><a href="/Arabidopsis_thaliana/Info/Annotation/" class="nodeco"><img src="/i/24/info.png" alt="" class="homepage-link">More information and statistics</a>
				</p>

